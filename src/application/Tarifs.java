package application;


public class Tarifs {
    private String idTarif;
    private String fraisInscTarif;
    private String cotisTarif;
    private String remDurTarif;
    private String remModTarif;

    void setIdTarif(String value) {
        this.idTarif = value;
    }

    String getIdTarif() {
        return this.idTarif;
    }

    void setFraisInscTarif(String value) {
        this.fraisInscTarif = value;
    }

    String getFraisInscTarif() {
        return this.fraisInscTarif;
    }

    void setCotisTarif(String value) {
        this.cotisTarif = value;
    }

    String getCotisTarif() {
        return this.cotisTarif;
    }

    void setRemDurTarif(String value) {
        this.remDurTarif = value;
    }

    String getRemDurTarif() {
        return this.remDurTarif;
    }

    void setRemModTarif(String value) {
        this.remModTarif = value;
    }

    String getRemModTarif() {
        return this.remModTarif;
    }

}
