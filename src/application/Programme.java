package application;


public class Programme {
    public String idLecon;
    public String idSupport;
    public String idSeance;
    public String idGroupe;
    public String idHoraire;

    String getIdLecon() {
        return this.idLecon;
    }

    void setIdLecon(String value) {
        this.idLecon = value;
    }

    String getIdSupport() {
        return this.idSupport;
    }

    void setIdSupport(String value) {
        this.idSupport = value;
    }

    String getIdSeance() {
        return this.idSeance;
    }

    void setIdSeance(String value) {
        this.idSeance = value;
    }

    String getIdGroupe() {
        return this.idGroupe;
    }

    void setIdGroupe(String value) {
        this.idGroupe = value;
    }

    String getIdHoraire() {
        return this.idHoraire;
    }

    void setIdHoraire(String value) {
        this.idHoraire = value;
    }

}
