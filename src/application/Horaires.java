package application;



public class Horaires {
    public String idHoraire;
    public String isSession;
    public String idJours;
    public String idDebut;
    public String idFin;
    public String libelHoraire;

    String getIdHoraire() {
        return this.idHoraire;
    }

    void setIdHoraire(String value) {
        this.idHoraire = value;
    }

    String getIsSession() {
        return this.isSession;
    }

    void setIsSession(String value) {
        this.isSession = value;
    }

    String getIdJours() {
        return this.idJours;
    }

    void setIdJours(String value) {
        this.idJours = value;
    }

    String getIdDebut() {
        return this.idDebut;
    }

    void setIdDebut(String value) {
        this.idDebut = value;
    }

    String getIdFin() {
        return this.idFin;
    }

    void setIdFin(String value) {
        this.idFin = value;
    }

    String getLibelHoraire() {
        return this.libelHoraire;
    }

    void setLibelHoraire(String value) {
        this.libelHoraire = value;
    }

}
