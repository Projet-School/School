package application;


public class Salles {
    public String idSalle;
    public String libelSalle;
    public String capaSalle;
    public String dispoSalle;

    void setIdSalle(String value) {
        this.idSalle = value;
    }

    String getIdSalle() {
        return this.idSalle;
    }

    void setLibelSalle(String value) {
        this.libelSalle = value;
    }

    String getLibelSalle() {
        return this.libelSalle;
    }

    void setCapaSalle(String value) {
        this.capaSalle = value;
    }

    String getCapaSalle() {
        return this.capaSalle;
    }

    void setDispoSalle(String value) {
        this.dispoSalle = value;
    }

    String getDispoSalle() {
        return this.dispoSalle;
    }

}
